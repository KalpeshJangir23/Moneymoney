import java.util.Scanner;

/**
 * B_Yet_Another_Coin_Problem
 */
public class B_Yet_Another_Coin_Problem {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int t =scanner.nextInt() ;
        while (t>0) {
            t--;
        } 
            scanner.close();
    }
}